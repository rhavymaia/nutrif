package br.edu.ifpb.ifnutri.entidade;

public class DadosVCT {
	
	private double peso;

	private double altura;

	private int nivelEsporte;

	public double getPeso() {
		return peso;
	}

	public void setPeso(double peso) {
		this.peso = peso;
	}

	public double getAltura() {
		return altura;
	}

	public void setAltura(double altura) {
		this.altura = altura;
	}

	public int getNivelEscporte() {
		return nivelEsporte;
	}

	public void setNivelEscporte(int nivelEscporte) {
		this.nivelEsporte = nivelEscporte;
	}
	

}
